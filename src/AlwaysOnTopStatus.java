public class AlwaysOnTopStatus {
    boolean alwaysOnTop;
    public AlwaysOnTopStatus() {
        setAlwaysOnTop(true);
    }

    public void setAlwaysOnTop(boolean alwaysOnTop) {
        this.alwaysOnTop = alwaysOnTop;
    }

    public boolean isAlwaysOnTop() {
        return alwaysOnTop;
    }
}
